public class DecisionMakingStmts {
    public static void main(String[] args) {
        int a= 2;
        switch (a) {
            case 1:System.out.println("a is matching to case 1");break;
            case 2:System.out.println("a is matching to case 2");break;
            case 3:System.out.println("a is matching to case 3");break;
            
            default:System.out.println("non of the cases are matched");
        }
    }
}
